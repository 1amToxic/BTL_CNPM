package model;

import java.io.Serializable;

/**
 *
 * @author lamit
 */
public class Field implements Serializable{
    private int id;
    private int quality;
    private String name;
    private int price;
    private String des;

    public Field() {
    }

    public Field(int quality, String name, int price, String des) {
        this.quality = quality;
        this.name = name;
        this.price = price;
        this.des = des;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getQuality() {
        return quality;
    }

    public void setQuality(int quality) {
        this.quality = quality;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }
    
    
}
